// 4.1 Площа кола
const circleRadius = 8;
const circleArea = Math.PI * circleRadius ** 2;
console.log("Circle area:", circleArea.toFixed(2));

// 4.2 Площа прямокутника
const lengthRectangle = 9;
const widthRectangle = 4;
const rectangleArea = lengthRectangle * widthRectangle;
console.log("Rectangle area:", rectangleArea.toFixed(2));

// 4.3 Обʼєм циліндра
const cylinderRadius = 3.5;
const heightCylinder = 8;
const cylinderVolume = Math.PI * cylinderRadius ** 2 * heightCylinder;
console.log("Cylinder volume:", cylinderVolume.toFixed(2));
